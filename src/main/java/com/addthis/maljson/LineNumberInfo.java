package com.addthis.maljson;

public class LineNumberInfo {

    final int line;
    final int column;

    public static final LineNumberInfo MissingInfo = new LineNumberInfo(-1, -1);

    public LineNumberInfo(int line, int column) {
        this.line = line;
        this.column = column;
    }

    public int getLine() {
        return line;
    }

    public int getColumn() {
        return column;
    }

    @Override
    public String toString() {
        return "LineNumberInfo{line=" + Integer.toString(line) + ",column=" + Integer.toString(column) + "}";
    }

}
